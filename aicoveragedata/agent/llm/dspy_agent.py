import os

from ..context.tools import (
    TASK_CODEBASE_HTML,
    TASK_DATASET_RANKING,
    TASK_DATASET_SCHEMA,
    TASK_REGRESSION_MODEL,
    classify_context_task,
    collect_context,
)
from ..core.config import AgentConfig, STATE_DIR
from ..memory.history import format_history


def _prepare_dspy_env():
    cache_dir = STATE_DIR / "dspy_cache"
    cache_dir.mkdir(parents=True, exist_ok=True)
    os.environ.setdefault("DSPY_CACHEDIR", str(cache_dir))
    os.environ.setdefault("LITELLM_LOCAL_MODEL_COST_MAP", "True")


def load_dspy():
    _prepare_dspy_env()
    try:
        import dspy
    except ModuleNotFoundError as error:
        raise ModuleNotFoundError(
            "DSPy is not installed. Run: .venv/bin/pip install -r aicoveragedata/agent/requirements.txt"
        ) from error
    return dspy


def dspy_model_name(model):
    return model if "/" in model else f"openai/{model}"


def create_dspy_lm(config: AgentConfig):
    dspy = load_dspy()
    lm = dspy.LM(
        dspy_model_name(config.model),
        api_key=config.api_key,
        cache=True,
    )
    return dspy, lm


def trim_context(context, max_chars):
    if len(context) <= max_chars:
        return context
    return context[:max_chars] + "\n\n[Context trimmed because it exceeded the configured limit.]"


def build_dataset_agent(dspy, config: AgentConfig, lm):
    class DatasetRankingAnswer(dspy.Signature):
        """Answer ranking questions using verified local tables.

        Rules:
        - Use context first, then history for follow-up meaning.
        - For top/lowest ranked tables, use the first relevant row.
        - Do not invent countries, industries, rankings, or values.
        - If context is insufficient, say so briefly.
        - Keep the answer short and concrete.
        """

        history = dspy.InputField(desc="Recent chat history. Use it to resolve follow-ups like 'what about highest?'.")
        question = dspy.InputField(desc="The user's ranking question.")
        context = dspy.InputField(desc="Verified ranked dataset context generated by agent tools.")
        answer = dspy.OutputField(desc="A short grounded answer with the first relevant ranked row.")

    class RegressionAnswer(dspy.Signature):
        """Explain model and regression results using generated model outputs.

        Rules:
        - State the target column when relevant.
        - Use held-out test metrics for verdicts unless asked about training.
        - Do not claim causality from R Square or feature importance alone.
        - Keep the answer short and concrete.
        """

        history = dspy.InputField(desc="Recent chat history.")
        question = dspy.InputField(desc="The user's regression, tree, XGBoost, or model question.")
        context = dspy.InputField(desc="Verified regression CSV outputs and model context.")
        answer = dspy.OutputField(desc="A grounded model-analysis answer.")

    class CodebaseAnswer(dspy.Signature):
        """Answer codebase and internal HTML questions using local file snippets.

        Rules:
        - Use snippets silently as evidence; do not narrate where you searched.
        - Cite file names or paths only when the user asks where code lives, asks for files, or asks for implementation locations.
        - Separate what is known from what would need inspection.
        - Do not invent files, routes, functions, or UI elements.
        - Keep the answer short and concrete.
        """

        history = dspy.InputField(desc="Recent chat history.")
        question = dspy.InputField(desc="The user's codebase or internal HTML question.")
        context = dspy.InputField(desc="Verified local code and HTML snippets.")
        answer = dspy.OutputField(desc="A grounded answer with file references when useful.")

    class GeneralDatasetAnswer(dspy.Signature):
        """Answer general dataset questions using verified local context.

        Rules:
        - Use exact column names when useful.
        - Do not invent values, causes, or fields.
        - If context is insufficient, say so briefly.
        - Keep the answer short and concrete.
        """

        history = dspy.InputField(desc="Recent chat history.")
        question = dspy.InputField(desc="The user's dashboard or dataset question.")
        context = dspy.InputField(desc="Verified local dataset context.")
        answer = dspy.OutputField(desc="A short grounded answer.")

    class DatasetAgent(dspy.Module):
        def __init__(self):
            self.ranking_answer = dspy.Predict(DatasetRankingAnswer)
            self.regression_answer = dspy.Predict(RegressionAnswer)
            self.codebase_answer = dspy.Predict(CodebaseAnswer)
            self.general_answer = dspy.Predict(GeneralDatasetAnswer)
            self.lm = lm

        def predictor_for_task(self, task):
            if task == TASK_DATASET_RANKING:
                return self.ranking_answer
            if task == TASK_REGRESSION_MODEL:
                return self.regression_answer
            if task == TASK_CODEBASE_HTML:
                return self.codebase_answer
            return self.general_answer

        def forward(self, question, history_messages=None):
            history_messages = history_messages or []
            task = classify_context_task(question, history_messages)
            context = trim_context(
                collect_context(question, history_messages=history_messages, task=task),
                config.max_context_chars,
            )
            history = format_history(history_messages) or "No previous messages in this chat."
            prediction = self.predictor_for_task(task)(history=history, question=question, context=context, lm=self.lm)
            answer = getattr(prediction, "answer", None) or "No text response returned by DSPy."
            return dspy.Prediction(answer=str(answer).strip(), context=context, task=task)

    return DatasetAgent()


def answer_with_dspy(question, history_messages, config: AgentConfig):
    dspy, lm = create_dspy_lm(config)
    agent = build_dataset_agent(dspy, config, lm)
    result = agent(question=question, history_messages=history_messages)
    answer = getattr(result, "answer", None)
    context = getattr(result, "context", "")
    return str(answer).strip() if answer else "No text response returned by DSPy.", context
